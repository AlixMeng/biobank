package edu.ualberta.med.biobank.tools.freezer_link;

public class BbpdbSpecimenInfo {

    private String studyNameShort;
    private String clinicName;
    private String chrNr;
    private String dateTaken;
    private String dateReceived;
    private String worksheet;
    private String inventoryId;
    private String linkDate;
    private String sampleName;
    private String phlebotomistId;
    private String consentSurveillance;
    private String consentGenetics;

    public String getStudyNameShort() {
        return studyNameShort;
    }

    public void setStudyNameShort(String studyNameShort) {
        this.studyNameShort = studyNameShort;
    }

    public String getClinicName() {
        return clinicName;
    }

    public void setClinicName(String clinicName) {
        this.clinicName = clinicName;
    }

    public String getChrNr() {
        return chrNr;
    }

    public void setChrNr(String chrNr) {
        this.chrNr = chrNr;
    }

    public String getDateTaken() {
        return dateTaken;
    }

    public void setDateTaken(String dateTaken) {
        this.dateTaken = dateTaken;
    }

    public String getDateReceived() {
        return dateReceived;
    }

    public void setDateReceived(String dateReceived) {
        this.dateReceived = dateReceived;
    }

    public String getWorksheet() {
        return worksheet;
    }

    public void setWorksheet(String worksheet) {
        this.worksheet = worksheet;
    }

    public String getInventoryId() {
        return inventoryId;
    }

    public void setInventoryId(String inventoryId) {
        this.inventoryId = inventoryId;
    }

    public String getLinkDate() {
        return linkDate;
    }

    public void setLinkDate(String linkDate) {
        this.linkDate = linkDate;
    }

    public String getSampleName() {
        return sampleName;
    }

    public void setSampleName(String sampleName) {
        this.sampleName = sampleName;
    }

    public String getPhlebotomistId() {
        return phlebotomistId;
    }

    public void setPhlebotomistId(String phlebotomistId) {
        this.phlebotomistId = phlebotomistId;
    }

    public String getConsentSurveillance() {
        return consentSurveillance;
    }

    public void setConsentSurveillance(String consentSurveillance) {
        this.consentSurveillance = consentSurveillance;
    }

    public String getConsentGenetics() {
        return consentGenetics;
    }

    public void setConsentGenetics(String consentGenetics) {
        this.consentGenetics = consentGenetics;
    }

}
